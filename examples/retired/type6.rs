fn main() {
    let case1 = Masked::from_str("A♠ J♥ T♠ 8♥").unwrap();

    println!("Auditing Case #1: {case1}");

    let baseline = HUPResult::from(&case1.shu);

    dump(&case1, &baseline);
    println!("=================");
    println!("=================");

    // let conn = Connection::open("generated/hups.db").unwrap();

    for shift in case1.other_shifts() {
        let hupr = HUPResult::from(&shift.shu);
        dump(&case1, &hupr);
    }
}

